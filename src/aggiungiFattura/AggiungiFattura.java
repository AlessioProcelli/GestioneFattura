    private void aggiuntaFatturaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aggiuntaFatturaActionPerformed
        if(iban.getText().equals("") || banca.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Tutti i campi devono essere compilati!","Errore",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(aggiungiFatturaModel.getClienteSelezionato() instanceof ClientePubblico)
            if(cig.getText().equals("") || cup.getText().equals("") || identificativo.getText().equals("")){
                JOptionPane.showMessageDialog(this,"Tutti i campi devono essere compilati!","Errore",JOptionPane.ERROR_MESSAGE);
                return;
            }
        if(aggiungiFatturaModel.getProdotti().isEmpty()){
            JOptionPane.showMessageDialog(this,"Devi inserire almeno un prodotto!","Errore",JOptionPane.ERROR_MESSAGE);
            return;
        }    
        String ibanSelezionato=iban.getText();
        String bancaSelezionata=banca.getText();
        String pagamentoSelezionato=pagamento.getSelectedItem().toString();
        Pagamento pagamento=null;
        if(pagamentoSelezionato.equals("VF"))
            pagamento=Pagamento.vistaFattura;
        else
            pagamento=Pagamento.valueOf(pagamentoSelezionato);
        Fattura nuovaFattura=null;
        MetodoPagamento metodoPagamento= new MetodoPagamento(pagamento, bancaSelezionata, ibanSelezionato);
        if(aggiungiFatturaModel.getClienteSelezionato() instanceof ClientePubblico)
            if(fattura.isSelected())
                nuovaFattura=new FatturaPubblica(aggiungiFatturaModel.getClienteSelezionato(), Integer.parseInt(numeroFattura.getText()), dataEmissioneFattura.getDate(), aggiungiFatturaModel.getProdotti(), Tipologia.Fattura, metodoPagamento, dataScadenza.getDate(), cig.getText(), cup.getText(), identificativo.getText(), dataEmissioneGara.getDate());
            else
                nuovaFattura=new FatturaPubblica(aggiungiFatturaModel.getClienteSelezionato(), Integer.parseInt(numeroFattura.getText()), dataEmissioneFattura.getDate(), aggiungiFatturaModel.getProdotti(), Tipologia.NotaDiCredito, metodoPagamento, dataScadenza.getDate(), cig.getText(), cup.getText(), identificativo.getText(), dataEmissioneGara.getDate());
        else
            if(fattura.isSelected())
                nuovaFattura=new FatturaBusiness(aggiungiFatturaModel.getClienteSelezionato(), Integer.parseInt(numeroFattura.getText()), dataEmissioneFattura.getDate(), aggiungiFatturaModel.getProdotti(), Tipologia.Fattura, metodoPagamento, dataScadenza.getDate());
            else
                nuovaFattura=new FatturaBusiness(aggiungiFatturaModel.getClienteSelezionato(), Integer.parseInt(numeroFattura.getText()), dataEmissioneFattura.getDate(), aggiungiFatturaModel.getProdotti(), Tipologia.NotaDiCredito, metodoPagamento, dataScadenza.getDate());
        
        Gateway g=GatewayConcreto.getInstance();
        g.memorizzaFattura(nuovaFattura);
        aggiungiFatturaController.fatturaAggiunta(dataEmissioneFattura.getDate());
        cig.setText("");
        cup.setText("");
        imponibile.setText("0");
        iva.setText("0");
        prezzoTotale.setText("0");
        identificativo.setText("");
        if(salvataggio.getState())
            g.memorizzaMetodoPagamento(metodoPagamento);
        else{
            iban.setText(g.getMetodoPagamento().getIban());
            banca.setText(g.getMetodoPagamento().getBanca());
            this.pagamento.setSelectedItem(g.getMetodoPagamento().getMetodo().toString());
        }
        JOptionPane.showMessageDialog(this,"Fattura aggiunta con successo!");
    }//GEN-LAST:event_aggiuntaFatturaActionPerformed

