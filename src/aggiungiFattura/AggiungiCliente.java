    private void aggiungiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aggiungiActionPerformed
        if(nomeAzienda.getText().equals("") || via.getText().equals("") || partitaIva.getText().equals("") || cap.getText().equals("") || codiceFiscale.getText().equals("") || città.getText().equals("") || provincia.getText().equals("") || nazione.getText().equals("") || email.getText().equals("") || codiceDestinatario.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Tutti i campi devono essere compilati!","Errore",JOptionPane.ERROR_MESSAGE);
            return;
        }    
        if(pubblico.isSelected())
            if(codiceAnagrafico.getText().equals("")){
                JOptionPane.showMessageDialog(this,"Tutti i campi devono essere compilati!","Errore",JOptionPane.ERROR_MESSAGE);
                return;
            }
        this.dispose();
        Gateway g=GatewayConcreto.getInstance();
        int id=g.getIdCliente();
        Cliente cliente=null;
        if (pubblico.isSelected()){
            cliente=new ClientePubblico(id, nomeAzienda.getText(), via.getText(),città.getText(), provincia.getText(), cap.getText(), partitaIva.getText(),codiceFiscale.getText(), email.getText(), codiceDestinatario.getText(),nazione.getText(), codiceAnagrafico.getText());
        }                                                                            
        else{
            cliente=new ClienteBusiness(id, nomeAzienda.getText(), via.getText(),città.getText(), provincia.getText(), cap.getText(), partitaIva.getText(),codiceFiscale.getText(), email.getText(), codiceDestinatario.getText(),nazione.getText());
        }
        af.clienteAggiunto(cliente);
            
    }//GEN-LAST:event_aggiungiActionPerformed

